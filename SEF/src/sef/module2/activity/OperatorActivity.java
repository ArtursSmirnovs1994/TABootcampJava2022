package sef.module2.activity;

/**
 * @author 
 *
 */
public class OperatorActivity {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		// Declare numbers to be operated
		int i = 8;
		int j = 5;
		
		// Subtract numbers
		int result = i-j;
		
		// Print result
		System.out.println("Difference = " + result);
		
		// Add numbers
		int addresult = i+j;

		// Print result
		System.out.println("Difference = " + addresult);

		// Multiple numbers
		int multipleresult = i*j;

		// Print result
		System.out.println("Difference = " + multipleresult);

		// Divide numbers
		int divideresult = i/j;

		// Print result
		System.out.println("Difference = " + divideresult);

		// Mod numbers
		int modresult = i%j;

		// Print result
		System.out.println("Difference = " + modresult);
		
		
	}

}
